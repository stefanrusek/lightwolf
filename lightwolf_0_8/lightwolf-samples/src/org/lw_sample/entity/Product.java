package org.lw_sample.entity;

public class Product {

    private String id;
    private double quantity;

    public String getId() {
        return id;
    }

    public void setId(String value) {
        id = value;
    }

    public double getQuantity() {
        return quantity;
    }

    public void setQuantity(double value) {
        quantity = value;
    }
}
